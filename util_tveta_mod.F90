  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%VFE_RDETAH (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%VFE_RDETAH,1):UBOUND(YD%VFE_RDETAH,1)))
  READ (KLUN) ZTMP1
  YD%VFE_RDETAH = ZTMP1
