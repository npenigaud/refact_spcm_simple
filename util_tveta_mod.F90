  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LVDETAH
IF (LVDETAH) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%VDETAH (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%VDETAH,1):UBOUND(YD%VDETAH,1)))
  READ (KLUN) ZTMP1
  YD%VDETAH = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
END SUBROUTINE


