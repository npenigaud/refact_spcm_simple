SUBROUTINE LOAD_TVETA (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TVETA), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LVDETAH, LVDETA_RATIO, LVETAF, LVETAH, LVFE_ETAF, LVFE_ETAH, LVFE_RDETAH
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LVETAH
IF (LVETAH) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%VETAH (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%VETAH,1):UBOUND(YD%VETAH,1)))
  READ (KLUN) ZTMP1
  YD%VETAH = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
