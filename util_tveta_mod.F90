IF (LVFE_ETAH) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%VFE_ETAH (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%VFE_ETAH,1):UBOUND(YD%VFE_ETAH,1)))
  READ (KLUN) ZTMP1
  YD%VFE_ETAH = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LVETAF
IF (LVETAF) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%VETAF (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%VETAF,1):UBOUND(YD%VETAF,1)))
  READ (KLUN) ZTMP1
  YD%VETAF = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LVFE_ETAF
IF (LVFE_ETAF) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%VFE_ETAF (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%VFE_ETAF,1):UBOUND(YD%VFE_ETAF,1)))
  READ (KLUN) ZTMP1
  YD%VFE_ETAF = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LVFE_RDETAH
