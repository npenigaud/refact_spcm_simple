  ALLOCATE (ZTMP3(LBOUND(YD%RAERSS,1):UBOUND(YD%RAERSS,1), LBOUND(YD%RAERSS,2):UBOUND(YD%RAERSS,2), LBOUND(YD%RAERSS,3):UBOUND(YD%RAERSS,3)))
  READ (KLUN) ZTMP3
  YD%RAERSS = ZTMP3
  DEALLOCATE (ZTMP3)
ELSE
  NULLIFY (YD%RAERSS)
ENDIF
READ (KLUN) LRAERSU
IF (LRAERSU) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%RAERSU (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%RAERSU,1):UBOUND(YD%RAERSU,1), LBOUND(YD%RAERSU,2):UBOUND(YD%RAERSU,2), LBOUND(YD%RAERSU,3):UBOUND(YD%RAERSU,3)))
  READ (KLUN) ZTMP3
  YD%RAERSU = ZTMP3
  DEALLOCATE (ZTMP3)
ELSE
  NULLIFY (YD%RAERSU)
ENDIF
READ (KLUN) LRAERV
IF (LRAERV) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%RAERV (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%RAERV,1):UBOUND(YD%RAERV,1), LBOUND(YD%RAERV,2):UBOUND(YD%RAERV,2), LBOUND(YD%RAERV,3):UBOUND(YD%RAERV,3)))
  READ (KLUN) ZTMP3
  YD%RAERV = ZTMP3
  DEALLOCATE (ZTMP3)
ELSE
  NULLIFY (YD%RAERV)
ENDIF
READ (KLUN) LRLATV
IF (LRLATV) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RLATV (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%RLATV,1):UBOUND(YD%RLATV,1)))
  READ (KLUN) ZTMP1
  YD%RLATV = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (YD%RLATV)
ENDIF
ALLOCATE (ZTMP2(LBOUND(YD%RTAEBC,1):UBOUND(YD%RTAEBC,1), LBOUND(YD%RTAEBC,2):UBOUND(YD%RTAEBC,2)))
READ (KLUN) ZTMP2
YD%RTAEBC = ZTMP2
DEALLOCATE (ZTMP2)
