  ALLOCATE (ZTMP3(LBOUND(YD%RAERSD,1):UBOUND(YD%RAERSD,1), LBOUND(YD%RAERSD,2):UBOUND(YD%RAERSD,2), LBOUND(YD%RAERSD,3):UBOUND(YD%RAERSD,3)))
  READ (KLUN) ZTMP3
  YD%RAERSD = ZTMP3
  DEALLOCATE (ZTMP3)
ELSE
  NULLIFY (YD%RAERSD)
