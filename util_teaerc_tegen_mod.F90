SUBROUTINE LOAD_TEAERC_TEGEN (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEAERC_TEGEN), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1), IL3(3), IU3(3)
LOGICAL :: LRAERBC, LRAEROR, LRAERSD, LRAERSS, LRAERSU, LRAERV, LRLATV
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
READ (KLUN) ZTMP0
YD%REPAER = ZTMP0
READ (KLUN) LRAERBC
IF (LRAERBC) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%RAERBC (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%RAERBC,1):UBOUND(YD%RAERBC,1), LBOUND(YD%RAERBC,2):UBOUND(YD%RAERBC,2), LBOUND(YD%RAERBC,3):UBOUND(YD%RAERBC,3)))
  READ (KLUN) ZTMP3
  YD%RAERBC = ZTMP3
  DEALLOCATE (ZTMP3)
ELSE
  NULLIFY (YD%RAERBC)
ENDIF
READ (KLUN) LRAEROR
IF (LRAEROR) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%RAEROR (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%RAEROR,1):UBOUND(YD%RAEROR,1), LBOUND(YD%RAEROR,2):UBOUND(YD%RAEROR,2), LBOUND(YD%RAEROR,3):UBOUND(YD%RAEROR,3)))
  READ (KLUN) ZTMP3
  YD%RAEROR = ZTMP3
  DEALLOCATE (ZTMP3)
ELSE
  NULLIFY (YD%RAEROR)
ENDIF
READ (KLUN) LRAERSD
